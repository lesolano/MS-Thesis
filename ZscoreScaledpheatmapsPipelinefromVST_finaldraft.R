#libraries####
library(DESeq2)
library(tidyverse)
library(pheatmap)
library(biomaRt)
#import ENSG IDs and read counts Make a table with relevant annotations####
Exp1_countsimport<-read.csv(file = "HELArawcounts2021.csv", header = TRUE)

#Prepare cts and coldata objects#####
RawReadCounts<-Exp1_countsimport
rownames(RawReadCounts)<-c(RawReadCounts$gene_id)
cts<-RawReadCounts[,-1]
coldata<-read.csv(file = 'HELAcoldata2021.csv',header = TRUE)
row.names(coldata)<-c(coldata$X)
all(rownames(coldata) %in% colnames(cts))
all(rownames(coldata) == colnames(cts))

cts <- cts[, rownames(coldata)]
all(rownames(coldata) == colnames(cts))
dds <- DESeqDataSetFromMatrix(countData = cts,
                              colData = coldata,
                              design = ~ Condition_NBE)

featureData <- data.frame(gene=rownames(cts))
mcols(dds) <- DataFrame(mcols(dds), featureData)

#factor condition and set levels
dds$Condition_NBE <- factor(dds$Condition_NBE, levels = c("HeLa_Control","HeLa_0R","HeLa_8R"))

#Uncomment to apply prefilter to dds object of readcounts>9
keep <- rowSums(counts(dds)) >= 250
dds <- dds[keep,]
#Run DESEQ function on dds object, and save to the same object
dds <- DESeq(dds)

HeLa0RvsHeLaControl<-results(dds, contrast=c("Condition_NBE","HeLa_0R","HeLa_Control"), alpha = 0.05)
HeLa8RvsHeLaControl <- results(dds, contrast=c("Condition_NBE","HeLa_8R","HeLa_Control"), alpha = 0.05)
HeLa8RvsHeLa0R <- results(dds, contrast=c("Condition_NBE","HeLa_8R","HeLa_0R"), alpha = 0.05)

#variance stabilizing transformations. ####
#This is both faster and ranked SD plots look flatter for our data! (Tibshirani 1988; Huber et al. 2003; Anders and Huber 2010)
vsd <- vst(dds, blind=FALSE)

#Define column/sample annotations
HELAcolannotationsdf <- as.data.frame(coldata$HeatShockStatus)
colnames(HELAcolannotationsdf) <- c("Condition")
row.names(HELAcolannotationsdf) <-colnames(assay(vsd))

#Make  local "mart"
mart<- useDataset("hsapiens_gene_ensembl", useMart("ensembl"))

#start by converting the ENSG ID vsd counts to HGNC IDs
HELA_VSD_ENSG<-row.names(assay(vsd))

#Make an ENSG lookup table to convert known ENSG IDs to HGNC IDs
HELA_VSD_ENSGlookup <- getBM(
  mart = mart,
  attributes = c('ensembl_gene_id',
                 'gene_biotype','hgnc_symbol'),
  filter = 'ensembl_gene_id',
  values = HELA_VSD_ENSG,
  uniqueRows = TRUE)

#prep a column to recieve ENSG IDs, then inner_join with lookup table w/HGNC + biotypes
HELA_VSD_ENSG_colprep<-rownames_to_column(as.data.frame(assay(vsd)), "ensembl_gene_id")
HELA_VSD_ENSG_HGNC_BT<-inner_join(HELA_VSD_ENSG_colprep,HELA_VSD_ENSGlookup,by ="ensembl_gene_id", keep=FALSE)

#remove duplicate HGNC IDs and name rows by HGNC
HELA_VSD_ENSG_HGNC_BT<-HELA_VSD_ENSG_HGNC_BT[!duplicated(HELA_VSD_ENSG_HGNC_BT$hgnc_symbol), ]
row.names(HELA_VSD_ENSG_HGNC_BT)<-HELA_VSD_ENSG_HGNC_BT$hgnc_symbol

#matrix input without column annotation information
HELA_VSD_ENSG_HGNC_BTm<-as.matrix(HELA_VSD_ENSG_HGNC_BT[c(grepl("HeLa",colnames(HELA_VSD_ENSG_HGNC_BT)))])

#1000x4000 This looks fine but consider ggsave for higher quality image

Top250HeLa_VSD_hgnc_CR_CC<-pheatmap(HELA_VSD_ENSG_HGNC_BTm[1:250,], cluster_rows=TRUE, show_rownames=TRUE,
                                                  cluster_cols=TRUE, annotation_col=HELAcolannotationsdf,
                                                  scale = 'row', main = "Top 250 Z-Score Scaled VST Normalized Gene Counts in Heat-Stressed HeLa Cells")

